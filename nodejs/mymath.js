var MyMath = {
    //create function
    add:function(num1,num2)
    {
        return num1 + num2;
    },
    sub:function(num1,num2)
    {
        return num1 - num2;
    },
    mul:function(num1,num2)
    {
        return num1 * num2;
    },
    div:function(num1,num2)
    {
        return num1 / num2;
    }
};
module.exports = MyMath;